X1 = 1;
X2 = 2;
X3 = -6;
Y1 = 4;
Y2 = 7;
Y3 = 6;

L1 = @(x) ((x-X2).*(x-X3)) ./ ((X1-X2)*(X1-X3));
L2 = @(x) ((x-X1).*(x-X3)) ./ ((X2-X1)*(X2-X3));
L3 = @(x) ((x-X1).*(x-X2)) ./ ((X3-X1)*(X3-X2));

x = [X3:0.0001:X2];

F = Y1*L1(x) + Y2*L2(x) + Y3*L3(x);

plot([X1 X2 X3], [Y1 Y2 Y3], '*', x , F , '-')
